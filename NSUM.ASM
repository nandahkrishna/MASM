ASSUME CS:CODE,DS:DATA
DATA SEGMENT 
	COUNT DB 00H
	NUMBERS DB 10 DUP(?)
	CARRY DB 00H
	RESULT DB 00H
DATA ENDS
CODE SEGMENT
        ORG 1000H
START:  MOV AX,DATA
        MOV DS,AX
	MOV CL,00H
	MOV AX,0000H
	MOV DL,COUNT	
	MOV SI,OFFSET NUMBERS	
NOZERO:ADD AL,[SI]
	JNC NOCARRY
	INC CL
NOCARRY:INC SI
	DEC DL
	JNZ NOZERO	
	MOV SI,OFFSET RESULT
	MOV [SI],AL
	MOV SI,OFFSET CARRY
	MOV [SI],CL
       	MOV AH,4CH
        INT 21H
CODE ENDS
END START
