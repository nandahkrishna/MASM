ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	ORG 00H
	MAX DB 00H
DATA ENDS
CODE SEGMENT
	ORG 1000H
START:	MOV AX,DATA
	MOV DS,AX
	MOV AH,02H
	MOV CL,MAX
	INC CL
	MOV CH,00H
	MOV BL,30H
	MOV BH,30H
NEXT:	MOV DL,0DH
	INT 21H
	MOV DL,BH
	INT 21H
	MOV DL,BL
	INT 21H
	INC BL
	CMP BL,3AH
	JNZ SKIP
	MOV BL,30H
	INC BH
SKIP:	MOV AL,06H
DELAY1:	MOV DX,0FFFFH
DELAY2:	DEC DX
	JNZ DELAY2
	DEC AL
	JNZ DELAY1
	DEC CL
	JNZ NEXT
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
