ASSUME CS:CODE,DS:DATA
DATA SEGMENT
        ORG 00H
	COUNT DW 0AH
	STAR DB "*$"
	PASSWD DB 10 DUP(0)
	SUCCESS DB "SUCCESS$"
	FAILURE DB "FAILURE$"
DATA ENDS
CODE SEGMENT
        ORG 1000H
START:  MOV AX,DATA
        MOV DS,AX
        MOV SI,OFFSET PASSWD
        MOV CX,COUNT
NEXT:   MOV AH,08H
        INT 21H
        MOV BL,AL
        MOV AH,09H
        MOV DX,OFFSET STAR
        INT 21H
        MOV AL,[SI]
        CMP AL,BL
        JNZ EXIT
        INC SI
        DEC CX
        JNZ NEXT
	MOV AH,09H
	MOV DX,OFFSET SUCCESS
	INT 21H
	MOV AH,4CH
	INT 21H
EXIT:   MOV AH,09H
	MOV DX,OFFSET FAILURE
	INT 21H
	MOV AH,4CH
        INT 21H
CODE ENDS
END START
