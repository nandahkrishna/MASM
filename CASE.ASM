ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	COUNT EQU 00H
DATA ENDS 
CODE SEGMENT
START:	MOV AX,DATA
	MOV DS,AX
	MOV CX,COUNT
L1:	MOV AH,01H
	INT 21H
	CMP AL,60H
	JNC UPPER
	ADD AL,20H
	JMP SKIP
UPPER:	SUB AL,20H
SKIP:	MOV AH,02H
	MOV DL,AL
	INT 21H
	LOOP L1
	MOV AH,4CH
	INT 21H
CODE ENDS
END START