ASSUME CS:CODE,DS:DATA
DATA SEGMENT 
	COUNT DB 00H
	NUMBERS DB 10 DUP(0)
	RESULT DB 00H
DATA ENDS
CODE SEGMENT
        ORG 1000H
START:  MOV AX,DATA
        MOV DS,AX
	MOV SI,OFFSET NUMBERS
	MOV CL,COUNT
CARRY: MOV AL,[SI]
OTHER: INC SI
	DEC CL
	JZ FINISH
NONZERO:CMP AL,[SI]
	JC CARRY
	JMP OTHER
FINISH: MOV SI,OFFSET RESULT
	MOV [SI],AL
       	MOV AH,4CH
        INT 21H
CODE ENDS
END START
