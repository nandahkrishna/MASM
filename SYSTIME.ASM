ASSUME CS:CODE,DS:DATA
DATA SEGMENT 
	HOUR DB 01 DUP(?)
	MINUTE DB 01 DUP(?)
	SECOND DB 02 DUP(?)
DATA ENDS
CODE SEGMENT
        ORG 1000H
START:  MOV AX,DATA
        MOV DS,AX
	MOV AH,2CH
        INT 21H    
	MOV SI,OFFSET HOUR
        MOV [SI],CH
	MOV SI,OFFSET MINUTE
        MOV [SI],CL
	MOV SI,OFFSET SECOND
        MOV [SI],DH
	MOV AH,4CH
        INT 21H
CODE ENDS
END START
