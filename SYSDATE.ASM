ASSUME CS:CODE,DS:DATA
DATA SEGMENT 
	DAY DB 01 DUP(?)
	MONTH DB 01 DUP(?)
	YEAR DB 02 DUP(?)
DATA ENDS
CODE SEGMENT
        ORG 1000H
START:  MOV AX,DATA
        MOV DS,AX  
       	MOV AH,2AH
        INT 21H
	MOV SI,OFFSET DAY
        MOV [SI],DL
	MOV SI,OFFSET MONTH
        MOV [SI],DH
	MOV SI,OFFSET YEAR
        MOV [SI],CX
	MOV AH,4CH
        INT 21H
CODE ENDS
END START
